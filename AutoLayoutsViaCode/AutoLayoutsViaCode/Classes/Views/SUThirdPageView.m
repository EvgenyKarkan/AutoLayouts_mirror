#import "SUThirdPageView.h"

@implementation SUThirdPageView

- (id)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    if (self) {
        self.backgroundColor = [[UIColor grayColor] colorWithAlphaComponent:0.8f];
    }
    return self;
}

@end
