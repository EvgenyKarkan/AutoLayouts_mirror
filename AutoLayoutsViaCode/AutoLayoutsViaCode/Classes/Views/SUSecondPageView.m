#import "SUSecondPageView.h"

@implementation SUSecondPageView

- (id)initWithFrame:(CGRect)frame
{
	self = [super initWithFrame:frame];
	if (self) {
		self.backgroundColor = [[UIColor orangeColor] colorWithAlphaComponent:0.4f];
	}
	return self;
}

@end
